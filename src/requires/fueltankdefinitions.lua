    elseif fuelType == "space" then
        return { "Space Fuel T", "Uncommon Spa", "Advanced Spa", "Rare Space ", "Exotic Space", "Uncommon Opt", "Advanced Opt", "Rare Optimis", "Exotic Optim", "Uncommon Gra", "Advanced Gra", "Rare Gravity", "Exotic Gravi" }
    elseif fuelType == "rocket" then
        return { "Rocket Fuel ", "Uncommon Roc", "Advanced Roc", "Rare Rocket", "Exotic Rocke", "Uncommon Opt", "Advanced Opt", "Rare Optimis", "Exotic Optim", "Uncommon Gra", "Advanced Gra", "Rare Gravity", "Exotic Gravi" }
    end
    return {}
end
